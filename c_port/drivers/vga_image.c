#include "drivers.h"
#include "io.h"

#define VGA_VRAM ((volatile u8*)0xA0000)

static void vga_write_regs(const u8* regs) {
    outb(0x3C2, *regs++);
    for (u8 i = 0; i < 5; ++i) { outb(0x3C4, i); outb(0x3C5, *regs++); }
    outb(0x3D4, 0x11); outb(0x3D5, inb(0x3D5) & 0x7F);
    for (u8 i = 0; i < 25; ++i) { outb(0x3D4, i); outb(0x3D5, *regs++); }
    for (u8 i = 0; i < 9; ++i) { outb(0x3CE, i); outb(0x3CF, *regs++); }
    for (u8 i = 0; i < 21; ++i) { (void)inb(0x3DA); outb(0x3C0, i); outb(0x3C0, *regs++); }
    (void)inb(0x3DA); outb(0x3C0, 0x20);
}

static const u8 regs_13h[] = {
    0x63,0x03,0x01,0x0F,0x00,0x0E,0x5F,0x4F,0x50,0x82,0x54,0x80,0xBF,0x1F,0x00,0x41,0x00,0x00,0x00,0x00,0x00,0x00,0x9C,0x8E,0x8F,0x28,0x40,0x96,0xB9,0xA3,0xFF,0x00,0x00,0x00,0x00,0x00,0x40,0x05,0x0F,0xFF,0x00,0x01,0x02,0x03,0x04,0x05,0x06,0x07,0x08,0x09,0x0A,0x0B,0x0C,0x0D,0x0E,0x0F,0x41,0x00,0x0F,0x00,0x00
};

static const u8 regs_3h[] = {
    0x67,0x03,0x00,0x03,0x00,0x02,0x5F,0x4F,0x50,0x82,0x55,0x81,0xBF,0x1F,0x00,0x4F,0x0D,0x0E,0x00,0x00,0x00,0x00,0x9C,0x8E,0x8F,0x28,0x0F,0x96,0xB9,0xA3,0xFF,0x00,0x00,0x00,0x00,0x00,0x10,0x0E,0x00,0xFF,0x00,0x01,0x02,0x03,0x04,0x05,0x14,0x07,0x38,0x39,0x3A,0x3B,0x3C,0x3D,0x3E,0x3F,0x0C,0x00,0x0F,0x08,0x00
};

void vga_image_view(const u8* bmp, u32 size) {
    if (size < 54 || bmp[0] != 'B' || bmp[1] != 'M') return;
    vga_write_regs(regs_13h);
    for (u32 i = 0; i < 320u * 200u; ++i) VGA_VRAM[i] = 0;

    u32 off = *(const u32*)&bmp[0x0A];
    u32 w = *(const u32*)&bmp[0x12];
    u32 h = *(const u32*)&bmp[0x16];
    if (w > 320u) w = 320u;
    if (h > 200u) h = 200u;

    const u8* pal = bmp + 54;
    outb(0x3C8, 0);
    for (u32 i = 0; i < 256u; ++i) {
        outb(0x3C9, pal[i * 4 + 2] >> 2);
        outb(0x3C9, pal[i * 4 + 1] >> 2);
        outb(0x3C9, pal[i * 4 + 0] >> 2);
    }

    const u8* src = bmp + off;
    for (u32 y = 0; y < h; ++y) {
        u32 dst_row = (h - 1u - y) * 320u;
        for (u32 x = 0; x < w; ++x) VGA_VRAM[dst_row + x] = src[y * w + x];
    }

    while (inb(0x60) != 0x01) {}
    while (!(inb(0x60) & 0x80)) {}
    vga_write_regs(regs_3h);
}
